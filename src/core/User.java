package core;

public class User {
	 String id;
	 String password;
	 String birth;
	 String phoneNumber;
	 String email;
	 String name;
	 int department_id;
	 int state_id;
	 int equip_rent_id;
	 int equip_rent_state;
	 String equip_request_day;
	 
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDepartment_id() {
		return department_id;
	}
	public void setDepartment_id(int department_id) {
		this.department_id = department_id;
	}
	public int getState_id() {
		return state_id;
	}
	public void setState_id(int state_id) {
		this.state_id = state_id;
	}
	public int getEquip_rent_id() {
		return equip_rent_id;
	}
	public void setEquip_rent_id(int equip_rent_id) {
		this.equip_rent_id = equip_rent_id;
	}
	public int getEquip_rent_state() {
		return equip_rent_state;
	}
	public void setEquip_rent_state(int equip_rent_state) {
		this.equip_rent_state = equip_rent_state;
	}
	public String getEquip_request_day() {
		return equip_request_day;
	}
	public void setEquip_request_day(String equip_request_day) {
		this.equip_request_day = equip_request_day;
	}
	
}
